package com.sonymobile.androidapp.gridcomputing.notifications;

/**
 * Identifier for the notification status..
 */
public enum NotificationStatus {

    /**
     * An identifier for the notification status when no notification was shown.
     */
    STATUS_NONE,

    /**
     * An identifier for the notification status of executing job.
     */
    STATUS_EXECUTING_JOB,

    /**
     * An identifier for the notification status of finished contribution.
     */
    STATUS_FINISHED

}
